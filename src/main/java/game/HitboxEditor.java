    private void addHitboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addHitboxActionPerformed
        HitboxPoint[] currentHitboxes;
        if((int)frame.getValue() == 0)
            currentHitboxes = modules[(int)module.getValue()-1].hitbox.resting;
        else
            currentHitboxes = modules[(int)module.getValue()-1].hitbox.animation[(int)frame.getValue()-1];
        HitboxPoint[] newHitboxes = new HitboxPoint[currentHitboxes.length+1];
        for(int i = 0; i < currentHitboxes.length; i ++){
            newHitboxes[i] = currentHitboxes[i];
        }
        newHitboxes[newHitboxes.length-1] = new HitboxPoint(40,40,10,1,100);
        
        if((int)frame.getValue() == 0)
            modules[(int)module.getValue()-1].hitbox.resting = newHitboxes;
        else
            modules[(int)module.getValue()-1].hitbox.animation[(int)frame.getValue()-1] = newHitboxes;
        hitbox.setValue(newHitboxes.length+1);
    }//GEN-LAST:event_addHitboxActionPerformed

