    public void changeModule(int newModule){
        frame.setModel(new SpinnerNumberModel(0, 0, modules[newModule].animation.length, 1));
        hitbox.setModel(new SpinnerNumberModel(1, 1, modules[newModule].hitbox.resting.length, 1));
        if(modules[newModule].hitbox.resting.length == 0 || modules[newModule].hitbox.resting[0].radius == 0){
            SpinnerNumberModel m = new SpinnerNumberModel(0,0,0,1);
            size.setModel(m); size.setEnabled(false);
            x.setModel(m); x.setEnabled(false);
            y.setModel(m); y.setEnabled(false);
            type.setModel(m); type.setEnabled(false);
            intensity.setModel(m); intensity.setEnabled(false);
        }
        else {
            size.setModel(new SpinnerNumberModel(1, modules[newModule].hitbox.resting[0].radius, 50, 1));
            x.setModel(new SpinnerNumberModel(0, modules[newModule].hitbox.resting[0].x, 80, 1));
            y.setModel(new SpinnerNumberModel(0, modules[newModule].hitbox.resting[0].y, 80, 1));
            type.setModel(new SpinnerNumberModel(1, modules[newModule].hitbox.resting[0].type, 10, 1));
            intensity.setModel(new SpinnerNumberModel(1, modules[newModule].hitbox.resting[0].intensity, 100, 1));
        }
    }
    
    

