    private void frameStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_frameStateChanged
        int moduleNum = (int)module.getValue()-1;
        HitboxPoint[] hitboxframe;
        if(moduleNum == 0)hitboxframe = modules[moduleNum].hitbox.resting;
        else hitboxframe = modules[moduleNum].hitbox.animation[moduleNum-1];
        
        hitbox.setModel(new SpinnerNumberModel(1, 1, hitboxframe.length, 1));
        if(hitboxframe.length == 0){
            SpinnerNumberModel m = new SpinnerNumberModel(0,0,0,1);
            size.setModel(m); size.setEnabled(false);
            x.setModel(m); x.setEnabled(false);
            y.setModel(m); y.setEnabled(false);
            type.setModel(m); type.setEnabled(false);
            intensity.setModel(m); intensity.setEnabled(false);
        }
        else {
            size.setModel(new SpinnerNumberModel(1, hitboxframe[0].radius, 50, 1));
            x.setModel(new SpinnerNumberModel(0, hitboxframe[0].x, 80, 1));
            y.setModel(new SpinnerNumberModel(0, hitboxframe[0].y, 80, 1));
            type.setModel(new SpinnerNumberModel(1, hitboxframe[0].type, 10, 1));
            intensity.setModel(new SpinnerNumberModel(1, hitboxframe[0].intensity, 100, 1));
        }
    }//GEN-LAST:event_frameStateChanged

    private void hitboxStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_hitboxStateChanged
        int moduleNum = (int)module.getValue()-1;
        HitboxPoint[] hitboxframe;
        if(moduleNum == 0)hitboxframe = modules[moduleNum].hitbox.resting;
        else hitboxframe = modules[moduleNum].hitbox.animation[moduleNum-1];
        
        int hitboxNum = (int)hitbox.getValue()-1;
        
        if(hitboxframe.length == 0){
            SpinnerNumberModel m = new SpinnerNumberModel(0,0,0,1);
            size.setModel(m); size.setEnabled(false);
            x.setModel(m); x.setEnabled(false);
            y.setModel(m); y.setEnabled(false);
            type.setModel(m); type.setEnabled(false);
            intensity.setModel(m); intensity.setEnabled(false);
        }
        else {
            size.setModel(new SpinnerNumberModel(1, hitboxframe[hitboxNum].radius, 50, 1));
            x.setModel(new SpinnerNumberModel(0, hitboxframe[hitboxNum].x, 80, 1));
            y.setModel(new SpinnerNumberModel(0, hitboxframe[hitboxNum].y, 80, 1));
            type.setModel(new SpinnerNumberModel(1, hitboxframe[hitboxNum].type, 10, 1));
            intensity.setModel(new SpinnerNumberModel(1, hitboxframe[hitboxNum].intensity, 100, 1));
        }
    }//GEN-LAST:event_hitboxStateChanged

